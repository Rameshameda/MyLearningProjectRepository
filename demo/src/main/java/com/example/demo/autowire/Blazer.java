package com.example.demo.autowire;

public class Blazer {
	String blazer;

	public String getBlazer() {
		return blazer;
	}

	public void setBlazer(String blazer) {
		this.blazer = blazer;
	}

	@Override
	public String toString() {
		return "Blazer [blazer=" + blazer + "]";
	}
	

}
